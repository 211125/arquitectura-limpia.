class Register {
  final String name;
  final String email;
  final String password;
  final String phone;
  final String address;
  final String state;
  final String city;
  final String calle;
  final String postal_code;
  final String interior_number;

  Register({
    required this.name,
    required this.email,
    required this.password,
    required this.phone,
    required this.address,
    required this.state,
    required this.city,
    required this.calle,
    required this.postal_code,
    required this.interior_number
  });

}

